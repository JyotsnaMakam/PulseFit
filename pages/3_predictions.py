import streamlit as st
import pandas as pd
from sklearn.linear_model import LinearRegression
import numpy as np

st.title("🤖 ML Calorie Predictor")

# 1. Dummy Training Data (In a real app, this would be a CSV)
# Features: [Duration in mins, Intensity (1-10)]
X = np.array([[10, 2], [20, 4], [30, 6], [40, 8], [60, 9]])
# Target: Calories burned
y = np.array([50, 120, 240, 400, 700])

# 2. Train the Model
model = LinearRegression()
model.fit(X, y)

# 3. User Input for Prediction
st.subheader("Predict your burn:")
dur = st.slider("Duration (mins)", 1, 120, 30)
intense = st.slider("Intensity Level (1-10)", 1, 10, 5)

# 4. Make the Prediction
if st.button("Predict"):
 prediction = model.predict([[dur, intense]])
# We use [0] to get the single number and avoid the "Ambiguous" error
 st.metric("Estimated Calories", f"{int(prediction[0])} kcal")
 st.success("Prediction generated by Linear Regression model!")